# what is a language?
# A language is a structured system of communication used by humans, consisting of spoken, written, or signed words and symbols. 
# It allows individuals to express thoughts, emotions, and ideas, facilitating interaction and understanding within a community. 
# Languages have their own rules of grammar, syntax, and vocabulary that govern how words are formed and combined to convey meaning.

# what is a programme ?
# A programme (or program) is a set of instructions written in a specific programming 
# language that a computer can execute to perform a particular task or solve a problem.

# what is a programming language?
# A programming language is a formal language comprising a set of instructions that can be used to produce various kinds of output.
# Programming languages are used in computer programming to implement algorithms and manipulate data structures. 

# Examples of popular programming languages include Python, Java, C++, JavaScript, and Ruby.

# what is python?
# Python is a high-level, interpreted programming language known for its simplicity and readability.    
# It was created by Guido van Rossum and first released in 1991. Python supports multiple programming paradigms,
# including procedural, object-oriented, and functional programming. It has a large standard library and a vibrant ecosystem of third-party packages,
# making it suitable for a wide range of applications, from web development and data analysis to artificial intelligence and scientific computing.  

